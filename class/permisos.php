class Permisos {

    //Atributos
    private $permiso;

    public function __construct() {
        $this->permiso = array();
    }

    /////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////////////
 
    public function listaPermisos(){
    
    
	 $sql = "SELECT * FROM permisos" ;
         $res = mysql_query($sql, Conectar::con());
         while ($reg= mysql_fetch_assoc($res)) {
             $this->permiso[] = $reg;
             
         }
         return $this->permiso;
  
    }
